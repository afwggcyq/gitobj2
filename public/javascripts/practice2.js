$(document).ready(function(){
    $("input#username,input#pass").focus(function(){
        $(this).css("background-color","green");
    });
    //点登录时的操作
    $("#focus-submit").click(function(){
        if ($("input#username").val() == ""){
            alert("请输入账号");
            $("input#username").focus();
        }
    });
});
